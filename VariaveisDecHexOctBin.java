package com.loiane.cursojava.aula11;

public class VariaveisDecHexOctBin {

	public static void main(String[] args) {
		
		int decVal = 26;
		int hexVal = 0x1a;
		int octVal = 032;
		int binVal = 0b11010;
		
		System.out.println("Variável Decimal: " + decVal);
		System.out.println("Variável Hexadecimal: " + hexVal);
		System.out.println("Variável Octal: " + octVal);
		System.out.println("Variável Binária: " + binVal);

	}

}
